	<div id="section2">
		<h2>2.Gokarna - Karnataka:-</h2>
		<img src="images/relaxing/gokarna.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Gokarna is a very small town in Uttar Kannada district, Karnataka. It is no less than Goa when it comes to relaxing holiday places. This place is known for peaceful beaches, but is mostly visited by youngsters looking for a quick break. Gokarna, also famous for Hindu temples, has something extraordinary to offer with luring beaches such as OM Beach, Kudle Beach, Half Moon Beach, Paradise Beach and Gokarna Beach. OM Beach, as its name suggests, looks like the OM symbol. Considered a paradise of adventure lovers, the beach has a beautiful ambiance and is a favorite spot for capturing stunning scenes. The best time to visit the beach is during the summer holidays in India.</p><p>Kudle Beach makes visitors enjoy with its remarkable sunrise. It is surprising to see coconut trees around the beach, which is just 6 kilometers away from bus stand. Half Moon Beach looks like a half moon and is another relaxing spot in Gokarna. This beach is away from the city crowd. You can enjoy swimming here. Paradise Beach, also known as the Full Moon Beach, is an extraordinary place where you can relish several activities. The tourists can visit the beach from the early morning hours until the late evening hours. Gokarna Beach is visited by many tourists since it is very close to Mahabaleshwar Temple. Gokarna is not expensive as Goa and can be visited during regional and public holidays in India.</p>
		
	</div>
	

	<div id="section3">
		<h2>3.Varkala - Kerala:-</h2>
		<img src="images/relaxing/varkala.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>What else can you expect other than a beach to relax? Varkala, a city in the Indian state of Kerala, is famous for its beaches like Goa and Gokarna. Varkala Beach is bounded by lush green and is exciting to watch. Once you visit this beach, you find a few tiny shops. Also known as Papanasam Beach, Varkala Beach is loved by the tourists seeking to relax. Varkala has become famous for the centers for Ayurvedic massage and also offers the best accommodation services. The major attractions of Varkala beach include Vishnu Temple, which is believed to have a history of 2000 years, water springs, etc.</p><p>Kappil Beach is not as big as other beaches in Varkala. This relaxing tourist point of Varkala has many things to be captured. Kappil Lake is a place where you can hire a boat for rent in order to explore the outstanding watercourse. Ponnumthuruthu Island, located just 20 kilometers away from the city of Varkala, has to be visited if you are a bird lover. You can also take a visit to Lord Shiva Temple here. Odayam Beach is the best relaxing holiday destination in the city. The ideal time to visit Varkala is during the summer holidays in India.</p>
		
	</div>
	

	<div id="section4">
		<h2>4.Almora - Uttarakhand:-</h2>
		<img src="images/relaxing/zero_Point(Almora).jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Almora is a small district in the Indian state of Uttarakhand. If you are in search of an interesting relaxing holiday spot in India, Almora should be in your list. Almora has Binsar Wildlife Sanctuary for animal lovers who can watch animals from a close distance. The sanctuary was built in order to protect animals in the year 1988. Spread across an area of 45 kilometers, the sanctuary has many animals. Almora is an ideal choice to visit during the festival or casual holidays in India.</p><p>The tourists of Almora have Zero Point to visit. Zero Point, surrounded by a peaceful surrounding, makes you feel relaxed and is popular for sunsets. Bright End Corner also makes you feel relaxed. Tourists like visiting this place because of its famous centers for meditation. Bright End Corner has been dedicated to Swami Vivekananda, who meditated here for some days.</p>
		
	</div>
	

	<div id="section5">
		<h2>5.Wayanad - Kerala:-</h2>
		<img src="images/relaxing/wayanad.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Wayanad is a district in the south Indian state of Kerala and has several tourist points such as Banasura Sagar Dam, Chembra Peak, Kuruva Island, Pookode Lake and many more. Banasura Sagar Dam is a paradise for the nature lovers. At the dam, you definitely enjoy boating. Needless to mention, trekking is very popular here. If you are touring with your children, be sure to take a visit to the nature park, which is favorite for kids.</p><p>Chembra Peak is a preferred spot for adventure lovers. Trekking is a key attraction of this spot. The major tourist destination of Wayanad, Kerala, is Pookode Lake situated at a distance of 15 kilometers from Kalpetta. Scattered across an area of 13 acres, the lake is enthralling to visit during holidays in India. An adult is charged an entry fee of Rs.10. The entry fee charged to kids is just Rs.5. </p>
	
	</div>

	<div id="section6">
		<h2>6.Mount Abu - Rajasthan</h2>
		<img src="images/relaxing/mount abu.jpg" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Mount Abu, a hill station in the state of Rajasthan, is a great relaxing holiday destination with astonishing tourist points such as Guru Shikhar, Achalgarh, Nakki Lake, Sunset Point, Wildlife Sanctuary, etc. Guru Shikhar is a better choice for trekking and photography. The spot remains open on all days of the week. No entry fee is charged. Nakki Lake attracts a large number of travelers throughout the year with wits peaceful atmosphere. Boat riding at this lake is very special. There is a garden near Nakki Lake to take a long walk and relax.</p><p>A majority of travelers do not miss visiting Sunset Point that has been featured even in a Bollywood movie. Mount Abu’s Wildlife Sanctuary is a heaven for nature lovers and bird watchers. The activities you can do here is a Jeep Safari. The entry fee for a jeep safari is Rs.300. Mount Abu also has some historically important temples. Families, friends and colleagues can visit Mount Abu to relax during the festival holidays in India or the regular holidays in India.</p>
	</div>

	<div id="section7">
		<h2>7.Gangtok - Sikkim</h2>
		<img src="images/relaxing/gangtok.jpg" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Gangtok is the capital city of Sikkim. Like other destinations discussed earlier, Gangtok has some exciting relaxing holiday spots such as Baba Mandir, Bakthang Waterfalls, Saramsa Garden, Tsomsgo Lake, Hanuman Tok, etc. Tsomsgo Lake is a magnificent spot. A distance of 40 kilometers has to be travelled from Gangtok to reach the take.</p><p>The Ban Jhakri offers a pleasant waterfall and you can see an artificial lake there. Cabs can be hired from Gangtok to reach the Ban Jhakri that remains open from 10 am to 5 pm. Gangtok has Tashi Viewpoint for nature lovers and Fambong La Wildlife Sanctuary for wildlife lovers..</p>
	</div>

