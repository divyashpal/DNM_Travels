		<h2>1.Goa:-</h2>
		<img src="images/relaxing/sinqureim beach GOA.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Goa is a destination for those who want to relax and those who want to party. It lures tourists throughout the country and the globe with its striking beaches, uniquely built churches, delicious food, and its greenery. Many tourists, including Bollywood stars, prefer visiting this spot to relax. The peak season for tourists is between October and January. This is when locals open up many shacks on the beach and provide a lot of water sports activities. Goa sees a high influx of tourists during Diwali, and during Christmas and New Year’s.</p><p>The capital city of Goa is Panaji, located at a distance of 593 kilometers from Mumbai and 592 kilometers from Bangalore. The state is divided into two districts: North Goa and South Goa. North Goa covers the most attractive tourist points. Calangute Beach, Baga Beach, Dona Paula, Margao, Baga Beach and Colva are a prime attraction in Goa to relax. Regular tourist spots include Baga, Calangute, Candolim, Anjuna and Arambol. However, if one is really seeking relaxation, there are many beaches that more secluded. South Goa is known to be more peaceful and less crowded. Some of the top beaches to visit in South Goa include Utorda, Majorda, and Betalbatim. Palolem beach in South Goa is a top tourist spot.</p>
