		<h2>1.Dussehra-Mysore:-</h2>
		<img src="images/festival/dusshera2.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Vijayadashami also known as Dussehra or Ayodhyapuja, is the ultimate celebration of the good's victory over evil. The Ramlila - an enactment of the life of Lord Rama, is held during the nine days preceding Dussehra. On the tenth day, larger effigies of Ravana, his son and brother - Meghnadh and Kumbhakarna are set to fire.</p><p>For the best and most immersive Dussehra experience, head to Mysore. Mysore Dasara is one of the most celebrated and pompous festival of India. During this period, the whole of Mysore undergoes serious changes and it is decorated beautifully with the streets lighted and the trees decorated. It attracts a huge audience both from all over India and all over the world.</p>
		
	</div>
	

	<div id="section2">
		<h2>2.Navratri-Gujarat:-</h2>
		<img src="images/festival/Gujarat.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>While Navratri is celebrated in different parts of the country, the way it is done is pretty different. In the south, it is celebrated as Golu with a display of dolls that symbolize feminine power. You wouldn't find dance to be a part of the celebrations. In the north, Navratri is often celebrated with Ramleela that depicts the tale of Ramayana and effigies of Ravana are burnt on Dushhera. However, in western part of India is where the real celebrations take place with all the music, dancing, garba and dandiya.</p>

        <p>Gujarat is the only place where all nine days of Navratri are celebrated with utmost enthusiasm. PGujarat. Women adorned in glittery outfits perform the folk dance Garbha and Dandiya around the pot.</p>
		
	</div>
	

	<div id="section3">
		<h2>3.Durga Puja-Kolkata:-</h2>
		<img src="images/festival/Kolkata.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Want to have encounter with rich wild animals of India, especially Tigers? Then Jim Corbett National Park is your destination. Home to some exotic wildlife animals, this 13184 sq.km spread national park is located in Nainital district of Uttarakhand. Tourists can take a jeep safari that would take you to the entire national park. If you are lucky enough, then you would encounter the royal Tiger of Jim Corbett. There are different zones- Dhikala Zone, Bijrani Zone, Sonanadi Zone and Durga Devi Zone.</p>
		
	</div>
	

	<div id="section4">
		<h2>4.Diwali-Varanasi:-</h2>
		<img src="images/festival/Diwali.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Diwali, known as the "Festival of Lights", is the biggest festival of the year in India. This five day festival commemorates the victory of good over evil, and is an occasion for prosperity and family togetherness.</p>

        <p>Varanasi is a crazy place at any time of year, but it becomes even more so during Diwali with a constant stream of firecrackers and fireworks going off all night long.</p>
		
	</div>
	

	<div id="section5">
		<h2>5. Eid-Delhi:-</h2>
		<img src="images/festival/eid.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Eid, or as it is officially known, Eid-al-Fitr. This day marks the end of the holy month of Ramadan, a time dedicated to fasting so that Muslims learn the importance of the things they might take for granted. It is also a time for generosity, and reflection.</p>

       <p>After the crescent moon is seen, the next day is considered to be the day of celebration. The day is dedicated not only to commemorate the end of fasting but also give thanks to Allah as the Quran was received by the Prophet Muhammad in this month.</p>
	
	</div>

	<div id="section6">
		<h2>6.Holi - Udaipur</h2>
		<img src="images/festival/holi.jpg"  alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>The royal palace in Udaipur is the best place to be on the eve of Holi, known as 'Holika Dahan', when the local Maharaja lights a ceremonial bonfire in the central courtyard to mark the start of the celebrations. Afterwards, guests retire for cocktails and a lavish dinner, followed by a firework display. Foreign visitors tend to congregate en masse the morning after in the 'chowk' below the nearby Jagdish temple to play with 'gulal' powders - the local tourism police maintain order so you don't see the kind of madness that prevails in the local bazaars.</p> 
		
	</div>

	<div id="section7">
		<h2>7.Baisakhi - Amritsar(Punjab)</h2>
		<img src="images/festival/Baisakhi.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>We all know that Amritsar is home to the most revered site of the Sikh community, the Harmandir Sahib Gurudwara, or what is popularly known as the Golden Temple. No doughnuts for guessing, this is the reason why this city is one of the best places for Baisakhi celebrations. While there's a congregation of massive proportions as devotees come to offer their prayers at the gurudwara, the area around the Golden Temple doubles up into a fair of sorts with plenty of stalls selling various articles, and delicious food.</p> 
		
	</div>

	<div id="section8">
		<h2>8.Janmashtami - Mathura</h2>
		<img src="images/festival/janmashtami.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Krishna Janmashtami, which is also known simply as Janmashtami.The annual Hindu festival celebrates the birth of Krishna, who is considered the eighth avatar of Vishnu.As per belief, Krishna was born in Mathura at midnight on the eighth day of Bhadrapada month, and the day is considered especially important to the Vaishnavism tradition of Hinduism. Devotees also sing devotional songs of love for Krishna, and keep a vigil way into the night.Apart from holding vigil, devotees also keep a fast on Janmashtami and they should only have a single meal the day before that. The fast lasts the whole day and is only broken the next day when both Rohini Nakshatra and Ashtami Tithi are over. The time to perform Krishna Puja is during Nishita Kaal, which is midnight as per Vedic time-keeping.</p> 
		
	</div>

