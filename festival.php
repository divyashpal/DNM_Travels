		<p>Amidst loud bellow of "Ganpati Bappa Morya, Mangal Murti Morya", the bustling city of Mumbai in  Maharashtra commences the Ganesh Chaturthi celebrations each year.You can witness the city come alive in vibrant colours, with idols being installed both at home and infamous pandals, namely Lalbaugcha Raja, Ganesh Galli Mumbaicha Raja,  KhetwadiGanraj, Siddivinayak Temple, Andhericha Raja, and others. On the last day of the Puja, in a carnival-like march, the idol is immersed in the Arabian Sea. </p> 
		
