		<h2>1.Taj Mahal - Uttar Pradesh:-</h2>
		<img src="images/historic/taj mahal.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Taj Mahal is declared as one of the 7 Wonders of the world. Located on the banks of river Yamuna, this ivory-white marble mausoleum was built by Shah Jahan in the memory of his wife Mumtaz. It is a monument of true love as the imperial court depicts the grief of Shah Jahan over Mumtaz's death. Mumtaz was Shah Jahan's most beloved wife out of 11 and she died delivering her 14th child. As per the last wish of Mumtaz, Shah Jahan built the Taj Mahal to signify their eternal love</p>
		
	</div>
	

	<div id="section2">
		<h2>2.Fatehpur Sikri - Uttar Pradesh:-</h2>
		<img src="images/historic/fatehpur.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>It is definitely a monument but Fatehpur Sikri is actually big enough to be considered a small city. Built during the reign of Akbar, Fatehpur Sikri is home to several iconic landmarks like Birbal's Palace, Tomb of Salim Chisti and Jama Masjid. Women who are childless come to the tomb of Salim Chisti to seek blessings.</p>
		
	</div>
	

	<div id="section3">
		<h2>3.Agra Fort - Uttar Pradesh:-</h2>
		<img src="images/historic/agra.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>This is a massive fort made by Akbar in 1565 AD and it stands out for its fabulous design and incredible construction. A magnificent piece of strategic design, only the Amar Singh Gate could be used to enter the fort out of the two gates. This fort is included in the top 10 historical places in India and is also mentioned in one of the cases of Sherlock Holmes written by Sir Arthur Conan Doyle called "The Sign Of Four".</p>
			</div>
	

	<div id="section4">
		<h2>4.Red Fort - Delhi:-</h2>
		<img src="images/historic/redfort.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Red fort is one of the famous monuments of India whose enormous size, aesthetic proportions and style, all represent an age of unrestrained opulence. It was also built by Shah Jahan and took over 10 years to complete. This fort used to be white as it was originally made with limestone but it started peeling off under the British rule and hence they painted it red.</p>
			</div>
	

	<div id="section5">
		<h2>5.Qutub Minar - Delhi:-</h2>
		<img src="images/historic/qutub minar.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Qutub Minar was built to depict the first Muslim kingdom in northern India. It stands at an incredible height of 240ft but the most interesting fact about this historical monument of India is that it has an  iron pillar that has not rusted despite the fact that it has been over 2,000 years. The best time to visit would be in October or November when the Qutub Minar festival is held. Don't forget to attend the light show in the evening. </p>
		</div>
	

	<div id="section6">
		<h2>6.Hawa Mahal - Rajasthan:-</h2>
		<img src="images/historic/hawa.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Hawa Mahal is considered to be one of the tallest buildings in the world without any foundation. Despite being curved, it still stands firm due to its pyramidal shape. This monument is known for the intricate lattice work on all of its 953 tiny windows. At first glance, it looks like a crown, which is a sight to behold.</p>
		
	</div>
	

	<div id="section7">
		<h2>7.Amer Fort - Rajasthan:-</h2>
		<img src="images/historic/amer.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Amer Fort is one of the top historical places in India. It is built with both red and white sandstone and the carvings on the ceiling and the walls are simply marvelous. The best part of the Amer Fort is the Sheesh Mahal (mirror palace) that has mirror mosaics and colored glasses.
        </p>
		
	</div>
	

	<div id="section8">
		<h2>8.Khajuraho Temples - Madhya Pradesh:-</h2>
		<img src="images/historic/khajuraho.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Khajuraho Temples is a group of Hindu and Jain temples built by the Chandelas reflecting the acceptance and respect for diverse religions during those times. They are famous for intricately carved statues and sculptures, some of which are remnants of the ancient culture of Kama Sutra.</p>
		
	</div>
	

	<div id="section9">
		<h2>9.Sanchi Stupa - Madhya Pradesh:-</h2>
		<img src="images/historic/sanchi.jpg" class="img-fluid" alt="adv" style="width:100%;" style="height:auto;">
		<p>The Sanchi stupa is an unparalleled example of the majestic aura that is encompassed within the Buddhist style of architecture. Ancient relics of Lord Buddha are rooted within the dome-shaped structure of the stupa. The 54 ft. high stupa tells the tales of Lord Buddha's life through its mesmerizing and intricate carvings.</p>
		</div>
	

	<div id="section10">
		<h2>10.Gwalior Fort - Madhya Pradesh:-</h2>
		<img src="images/historic/gwaliorfort.jpg" class="img-fluid" alt="adv" style=" widt:"100;%"style="hheigh:auto;%">
		<p>This amazingly built fort is considered one of the most impenetrable forts in the country. It has a long and turbulent history that dates back to as far back as 525 AD. The architecture and the history of this monument are intriguing and don't miss out on the sound and light show when you visit.</p>
		
	</div>
	

	<div id="section11">
		<h2>11.Konark Temple - Odisha:-</h2>
		<img src="images/historic/konark.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>This temple is built to worship the Sun God. It has 12 wheels at the base which are actually sundials that accurately measure time. This temple is built in the shape of a gigantic chariot and is a sight to behold.</p>
		
	</div>
	

	<div id="section12">
		<h2>12.Gateway of India - Maharashtra:-</h2>
		<img src="images/historic/gateway.jpg" alt="adv" class="img-fluid" style=" widt:"100;%"style="hheigh:auto;%">
		<p>This gateway is considered to be one of the most famous landmarks in the world. It was actually built as an entry and exit point during the British period but now has become a favorite picnic spot for locals and tourists. Several tourists visiting Mumbai prefer to book a stay around the vicinity of Gateway Of India and Taj Palace Hotels is one of the most famous hotels between the tourists. </p>
	
	</div>
	

	<div id="section13">
		<h2>13.Charminar - Hyderabad:-</h2>
		<img src="images/historic/charminar.jpg" alt="adv" class="img-fluid" style=" widt:"100;%"style="hheigh:auto;%">
		<p>Charminar was built to celebrate the end of the plague in the city and to depict that you can see a cat's head built on one of the arches to scare away rats that had once almost destroyed Hyderabad. This monument gets its name because of its four long pillars or minars that measure almost 20 meters. It is an exquisite site and is also considered mysterious since there is a secret tunnel from here to Golconda Fort.</p>
		
	</div>
	

	<div id="section14">
		<h2>14.The Ruins of Hampi - Karnataka:-</h2>
		<img src="images/historic/hampi.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>This place is a paradise for history and art lovers. It has several beautiful structures and carvings and it is also the place that  recorded the first instance of human settlement which dates all the way back to 1 CE.</p>
		
	</div>
	

	<div id="section15">
		<h2>15.Golconda Fort - Hyderabad:-</h2>
		<img src="images/historic/Golconda.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>This fort is one of the most famous historical landmarks in India. It is an archaeological treasure known for its magnificent architecture, magical acoustic effect, and imposing structure. The fort's mosques and colossal mud walls and its incredible acoustics are worth seeing and hearing.</p>
		
	</div>
	

	<div id="section16">
		<h2>16.The Great Living Chola Temples - Tamil Nadu:-</h2>
		<img src="images/historic/Chola.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>The three great temples made by the Chola empire are - Brihadeshwara Temple, Airavateshwara Temple, and GangaikondaTemple. All of them are the epitomes of artistic and architectural excellence and they reflect the outstanding achievements of the Chola Empire in sculpture, architecture, painting and bronze casting..</p>
		
	</div>
	

	<div id="section17">
		<h2>17.India Gate - Delhi:-</h2>
		<img src="images/historic/indiagate.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>India Gate is a 42-metre tall war memorial built to pay tribute to the soldiers of the British Indian Army who died during World War I. There are beautiful lawns surrounding the monument and a splendid light show at the fountains. You should visit on the Republic Day as there is a parade there every year.</p>
	
 	</div>
	

	<div id="section18">
		<h2>18.Victoria Memorial - Kolkata:-</h2>
		<img src="images/historic/Victoria.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Victoria Memorial, named after Queen Victoria, is an elegant white marble structure surrounded by lush green gardens which together cover 64 acres of land. There are small beautiful structures spread throughout the garden and the best time to roam around here is in the evening when the memorial is lit up with numerous lights.</p>
		
	</div>
	

	<div id="section19">
		<h2>19.Rani ki Vav, Patan - Gujarat:-</h2>
		<img src="images/historic/rani ki vav.jpg" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>This is a recent archaeological discovery in Patan and it is one of the most peculiar historical places in India. It has a very interesting stepwell and over 500 elaborate sculptures of Gods and Goddesses among seven galleries that are worth seeing.</p>
		
	</div>
	

	<div id="section20">
		<h2>20.Mysore Palace - Karnataka:-</h2>
		<img src="images/historic/mysorepalace.jpg" class="img-fluid" alt="adv" style="width:100%;" style="height:auto;">
		<p>This palace is so grand in its proportions and beauty that it looks like it sprung out of a fairytale. The interiors of the palace are intricately designed and adorned with ornate ceilings. There are jeweled corridors, stained glass windows, open mandaps, and also a jeweled throne. The best time to visit this palace is during the Dussehra festival.</p>
		
