		<h2>1.Coorg - Karnataka:-</h2>
		<img src="images/natural/coorg.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Famously known as the 'Scotland of India', Coorg is a tiny hill station located in Southern Karnataka in the lap of Western Ghats. This place is irresistible with its lush coffee plantations, stunning landscapes, quaint villages, refreshing weather, cascading waterfalls, like Abbey and Iruppu falls, and a diversity of flora and fauna at Nagarhole National Park and Talakaveri Wildlife Sanctuary. It is a perfect place if you're looking to escape the monotony of daily life and get close to nature.</p> 
		
	</div>
	

	<div id="section2">
		<h2>2.Khajjiar - Himachal Pradesh:-</h2>
		<img src="images/natural/khajjiar.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Commonly known as the 'Mini Switzerland of India', Khajjiar sits at over 1981 meters (6500 feet) and is synonymous with picturesque beauty. Dense forest, swathes of green meadows and beautiful snow-capped mountain vistas make it an ideal destination for nature lovers. Visitors can partake of adventure activities, like zorbing, trekking, paragliding and horse-riding, and the must-visit sites include the Kalatop Wildlife Sanctuary that is home to exotic flora and fauna, Khajji Naga Temple that dates back to the 12th century, and the Khajjiar Lake set at 1920 meters and encircled by hills, grasslands and deodar forests.</p>
		
	</div>
	

	<div id="section3">
		<h2>3.Pithoragarh - Uttarakhand:-</h2>
		<img src="images/natural/pithorogarh.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Located in the eastern district of Uttarakhand, Pithoragarh boasts of irresistible natural beauty, high altitude grasslands and historic charm. It is the starting point for treks to the holy shrines of Kailash and Mansarovar. And, the panoramic views of the mighty peaks of Nanda Devi, Appi of Nepal and Panchachuli are clearly visible from this place. Some of the tourist spots include the Pithoragarh Fort, Askot Sanctuary that is home to a wide range of flora and fauna, and Kafni Glacier Trek, among many others. And, visitors can also indulge in adventure activities, like hiking, skiing, canoeing, kayaking and fishing. This tiny place is an ideal place for those looking for a nature retreat.</p>
	
		
	</div>
	

	<div id="section4">
		<h2>4.Kaas Plateau - Maharashtra:-</h2>
		<img src="images/natural/kaas-plateau.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Another ethereal beauty is the Kaas Plateau, which is a UNESCO World Biodiversity Site. It attracts visitors with more than 850 different and unique species of flowering plants. When these flowering plants are in full bloom in the months of August-September, its beauty is incomparable. Being relatively undiscovered, you can experience this beauty in utmost serenity and breathe in the fresh, unpolluted air.</p>
		
	</div>
	

	<div id="section5">
		<h2>5.Munnar - Kerala:-</h2>
		<img src="images/natural/munnar.jpg" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>Spend some "me" time in nature's lap in Munnar, a hill station famous for its tea and spice plantations. Camping, parasailing, birdwatching, trekking, wildlife spotting, fishing, boating and rock climbing are the popular activities here. Some of the must-visit attractions include Echo Point, and Kundala Lake where you can enjoy boating and shikara rides amidst the natural beauty and views of rolling hills; Devikulum is home to an assortment of temples, Attakud waterfall and Anamudi Peak, among many others. For tea lovers, there's a Tata Tea Museum where you can learn about the history of tea and its processing. </p>
	
	</div>

	<div id="section6">
		<h2>6.Floating lake - Manipur</h2>
		<img src="images/natural/loktak-lake.jpg" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Loktak Lake of Manipur state is the largest freshwater lake in India,located near Moirang and mostly famous for the floating phumdis. This ancient lake also host the only floating national park in the world, Keibul Lamjao which is solo home of the endangered sangai or Manipur brow-antlered deer.</p>
	</div>

	<div id="section7">
		<h2>7.Chail - Himachal Pradesh</h2>
		<img src="images/natural/chail.jpg" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Sitting amid the Shivalik hills, Chail is a peaceful and secluded hill station that once served as a summer retreat for the Maharaja of Patiala. Surrounded by deodar and pine forests and replete with spectacular views of the mountains, this place is a delight for nature lovers and photographers. A visit to Sadhupul lake, Chail Palace, Kali temple and the Chail Wildlife Sanctuary is a must when in Chail.</p>
	</div>

	<div id="section8">
		<h2>8.Ziro - Arunachal Pradesh</h2>
		<img src="images/natural/ziro.jpg" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Unscathed by modern civilization, the Ziro in north-eastern India is blessed with abundant natural beauty. Vast expanses of paddy fields, rolling green hills with abundant flora, quaint villages and its virgin beauty attract many nature lovers. Besides, this place is also a treat for adventure seekers, trekkers and wildlife explorers. Some of the must-visit attractions include Talley Valley Wildlife Sanctuary that is home to a variety of exotic flora and fauna, Kile Pakho where you can soak in the natural beauty, and Dolo Mando and Midey are trekkers' paradise.</p>
	</div>

	<div id="section9">
		<h2>9.Araku - Andhra Pradesh</h2>
		<img src="images/natural/aarku-valley.jpg" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>The vast grasslands, rose gardens of varying hues, coffee fields, fruit-laden orchards, gushing waterfalls, and the diversity of flora and fauna mean Araku Valley is a spot with prolific scenic beauty. The famous attractions here include coffee plantations, Padmapuram botanical garden, a Tribal museum that features tribal lifestyle and handicrafts, and waterfalls, like Sangda falls, Dumbriguda falls and Katiki falls. Also, the natural Borra Caves situated near the valley are a must visit.</p>
	</div>

	<div id="section10">
		<h2>10.Dzukou Valley - Nagaland</h2>
		<img src="images/natural/dzukou-valley.jpg" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Sandwiched between north-eastern states of Manipur and Nagaland, Dzukou Valley is home to lush green forest, mosaic of grasslands, flowers of varying hues, and an abundance of flora and fauna, making it a perfect spot to spend some time in the lap of Mother Nature. The Japfu hills add to the splendour of the place and make it is a perfect spot for trekking. If you're not up for trekking, take a stroll among the ubiquitous flowers. This is home to a variety of flowers, including the rare and unique Dzukou lily, multi-colored rhododendrons and aconitum. The best time to visit is October to May. To reach the valley, you need to trek either from Zakhama village or Visvema village. Indian visitors need an inner line permit for Nagaland and foreign visitors must register themselves at the local state government office in Kohima or Dimapur, Nagaland.</p>

