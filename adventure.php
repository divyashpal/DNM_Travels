		<h2>9. Bungee Jumping in Rishikesh:-</h2>
		<img src="images/adventure/bungeerishikesh.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Jumping from the height of more than 83 meters which is roughly 273 feet and hanging in the middle of the sky and ground is thrilling experience one must enjoy. For those who never heard about bungee jumping, it is an activity that involves jumping from the tall structure connected with large elastic cord on your hips allowing you to have free falling experience. However, you need to be fully fit to conduct this adventure activity to avoid any physical injury.</p>
		<p><b>Location:</b> The activity is done at the Mohan Chatti village.</p>
		<p><b>Important Note:</b> For bungee jumping minimum weight required is 40kg and maximum 110kg.</p>
		<p><b>Price:</b> Basic price for Bungee Jumping is Rs 3500/- per person.</p>
