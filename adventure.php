		<h2>12.Dune Bashing in Jaisalmer:-</h2>
		<img src="images/adventure/dunebashing.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>If you have heard about dune bashing happening in Dubai sand and missing the action, then in India Jaisalmer is the place where you can enjoy this adrenaline adventure. A 4x4 SUV is led in the desert sitting inside which you will experience the dashing of sand coming its way. Enjoying a spell of dune bashing, Dubai-style, on the Sam sand dunes, 44 km from Jaisalmer in the midst of the Thar desert. Keep the traditional camel ride aside and get into the dune bashing in lavish style.</p>
		<p><b>Packges:</b> There are different packages offered including- Arabian Night with Dune Bashing for 2N/3D, Dune Bashing with Complete Jaisalmer for 1N/2D and Desert Camping 1N/2D. The cost of the package ranges from Rs 5,000 to Rs 10,000/- per person.</p>
		<p><b>Best Time:</b> From December to March is the ideal time for dune bashing.</p>
