	<div id="section5">
		<h2>5. Roopkund Trek in Uttarakhand:-</h2>
		<img src="images/adventure/roopkundtrek.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>If underline trekking is your favourite thing, then Roopkund Trek is the destination you should prefer. Located at the high altitude in Chamoli district of Uttarakhand, one can trek to the elevation of 3200m.The trek is known for energizing excursion and so which adventurers should tread this land to revitalize. Roopkund Trek is known for its magical landscape, which you will experience in every pass.
        Do not miss the 'Lake of Mysteries' which can be reached after passing range of forests and gushing brooks. One of the exciting parts of this trek is the 'Mystery Lake', also known as Roopkund Lake which is at the height of 15500 feet above the sea level and is the home of hundred skeletons.</p>
		<p><b>Location:</b> To explore this trek, you need to visit Chamoli districts which is located at the base of the two Himalayan peaks: Trisul and Nanda Ghunti.
        Best Time to visit: The best time to visit Roopkund Trek is between May-June just before the monsoon.</p>
		<p><b>Price:</b> If you're in a group and have booked with an adventure trip company, then the approx. price for per person would be Rs 10,500/- for 8-10 days which includes the accommodation, meals, trek equipment and other necessary permit fees.</p>
	</div>
	

	<div id="section6">
		<h2>6. Manali to Leh Bike Trip:-</h2>
		<img src="images/adventure/manalitoleh.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Imagine riding your bike across the valleys and never ending road, and the path giving you the majestic view of snowpacked mountain. Is not this a wonderful experience? A bike trip to Himalayan region offers high dose of adrenaline experience. The Manali to Leh bike trip is a chance to explore the journey of various hallucinating places.
        Stopover in sapphire lakes, canyon mountains, drink a hot tea at the tapri of this region. The delicate breeze of this route has something magical that makes bikers go beyond their capacity. The bike trek usually begins from Manali, a delightful town on the banks of Beas River and will continue to high-street routes including Khardungla pass, known as worlds most elevated motorbike street.</p>
		<p><b>Routes Covered:</b> Manali- Jispa- Sarchu- Leh- Khardungla Pass</p>
		<p><b>Best Time:</b> July to September is considered as the ideal period for bike trip for Manali to Leh.</p>
		<p><b>Price:</b> For Manali to Leh Bike Trip there are various adventure groups and excursion companies offering packages. The ideal package starts from Rs 20,000/- per person including accommodation, meals and other adventure activities. There are itineraries which needs to be followed. If you’re planning for solo adventure bike trek then the price would fluctuate according to your comfort.</p>
	</div>
	

	<div id="section7">
		<h2>7. Caving in Meghalaya:-</h2>
		<img src="images/adventure/caving.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>This North-Eastern India state is the most beautiful location in all sense. It holds some of the best treasures exploring them is worth every penny. Enchanting caves, beautiful waterfalls and majestic mountains. Caving in Meghalaya is the most happening adventure one can have. The Khasi Hills and Jaintia Hills are two most popular hills which are blessed with enigmatic caves. Other options to discover are- Krem Mawkhydrop, Krem Dam, Krem Lymput and Krem Mawjymbuin.</p>
		<p><b>Best Time:</b> The best time to explore Meghalaya caves is during winter months. December to March are the perfect months.
        Things to Remember: One needs to very specific to learn about different caving techniques, From being a sandwich to being a Spiderman should be your hidden talent.</p>
		<p><b>Stay:</b> You can stay at Mawphlang and Pine Farm where one get some good options. You can even choose Cherrapunji Holiday Resort that offers some great facilities to stay at.</p>
		<p><b>Package:</b> If visited through Meghalaya Tourism Development Corporation, then you travel in deluxe buses, sumo and tourist coaches to different caves and would cost not more than 175 per person.</p>
