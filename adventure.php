		<img src="images/adventure/hotair.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>If you are an adventure junkie and want some enthralling experience along with safety concern, then hot air balloon ride over the desert of Jaipur is adrenaline adventure you must go for. Once you get into the hot air balloon, you will enjoy the adventure of floating along the winds. Admire the birds eye view of the Pink City and get absorb into the colours of this city. Watching Hidden palaces and historical from the top angle will surely mesmerize you.</p>
		<p><b>Bets Time:</b> The best time to experience this adventure is during November to June. The timing of the flight is done in the early morning and in the afternoon. The safari would last for 1 hour.</p>
		<p><b>Service:</b> Sky Waltz Balloon Safari is the popular service in Jaipur.</p>
		<p><b>Price:</b> The standard flight ticket- Rs 12000 under Skyvisa.</p>
		<p><b>Under SkySail:</b> Rs 7500 (per adult) and Rs 6000 (per child.</p>
