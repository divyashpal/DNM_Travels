		<h2>1. Chadar Trek in Ladakh:-</h2>
		<img src="images/adventure/chadartrek.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Experience the walking over the frozen river facing various challenges. Chadar Trek in Ladakh is the thrilling experience one can have. Open only during the winter season, i.e. January- February, the trek takes place at the Zanskar River which freezes and gets covered with thick blanket of snow. During the trek, one can enjoy the canyon shape valleys bestowed with thick blanket of snow.</p>
		<p><b>Location:</b> Zanskar River, Ladakh</p>
		<p><b>Difficulty Level:</b> Difficult and the altitude can reach up to 1123ft.</p>
		<p><b>Price:</b> The package differs accroding to the number od days covered. The price would include the stay and other areas the will be covered. For 6N/7D, the approx. price starts from 24,000/- inclusive if tax and hotek rentals.</p>
	</div>
	

	<div id="section2">
		<h2>2. White River Rafting in Rishikesh:-</h2>
		<img src="images/adventure/whiteriverrafting.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>When we think of performing river adventure, Rishikesh stands out in the league. Rishikesh is the capital of river rafting in India, where the holy Ganga River flows, offering the speed of Grade I-V. River rafting in Rishikesh is an ultimate experience one can have. Hitting the fast gushing waters of Ganges gives exhilarating adventurous sports.
        While rafting on this holy river, you can enjoy the scintillating view of majestic Himalayas covered with lush green. Accompanied by professional rafters, you will be guided for an accident free rafting experience. While in Rishikesh one can have the tour of the most loved places of Rishikesh also!</p>
		<p><b>Locatin:</b> there are different locatins for river rafting. Brahmpuri to NIM Beach (9kms), Brahmpuri to Rishikesh (12kms), Shivpuri to Rishikesh (16kms), Marine Drive to Rishikesh (34kms).</p>
		<p><b>Price:</b> The price for per person could be around Rs 450-1850/- depending in the kms chosen.</p>
		<p><b>Best Time:</b> Except monsoon, one can go for river rafting in months between February to November.</p>
	</div>
	

	<div id="section3">
		<h2>3. Jim Corbet Safari in Uttarakhand:-</h2>
		<img src="images/adventure/jimcorbet.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Want to have encounter with rich wild animals of India, especially Tigers? Then Jim Corbett National Park is your destination. Home to some exotic wildlife animals, this 13184 sq.km spread national park is located in Nainital district of Uttarakhand. Tourists can take a jeep safari that would take you to the entire national park. If you are lucky enough, then you would encounter the royal Tiger of Jim Corbett. There are different zones- Dhikala Zone, Bijrani Zone, Sonanadi Zone and Durga Devi Zone.</p>
		<p><b>Best Time:</b> You can explore this national park from mid-October to mid-June. Visiting during monsoon isn’t a good idea.</p>
		<p><b>Locatin:</b> Ramnagar, Nainital District, Uttarakhand</p>
		<p><b>Price:</b> For Indian tourists- approx. Rs 4200/- including the jeep carrying 6 persons.</p>
		<p><b>For Foreigners:</b> Rs 8000/- for jeep and 6 persons.</p>
	</div>
	

	<div id="section4">
		<h2>4. Scuba Diving in Andaman:-</h2>
		<img src="images/adventure/scubaandaman.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Deep clean blue water and gorgeous marine life makes Andaman truly a magical place to explore. The marine life of Andaman is a lifetime experience to explore which is worth an adventure trip to have. From scorpion fish and Octopus to Sharks and Coral Reefs, there are many things which you cannot lose to miss. Some of the diving sites in Andaman are- The Wall (Havelock Island), Clinque Island (Port Blair) and Fish Rock (Passage Island).</p>
		<p><b>Best Time:</b> January to May is considered as the best time when the island remains clam and conditions are better for diving.</p>
		<p><b>Price:</b> The diving price in Andaman island for various categories is divided into different packages:<br><br>
                        Non- Swimmers- Approx. Rs 3500/- per person for 30 minutes<br>
                        Beginners course: Approx. Rs 4500/- per person for 60 minutes<br>
                        Certified Divers 2 days and 4 dives- Rs 10800/- per person<br>
                        Night Dive- Rs 3,500/- per person<br>
                        Single Dive- Rs 3,500/- person<br></p>
