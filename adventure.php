	<div id="section8">
		<h2>8. Skiing in Gulmarg:-</h2>
		<img src="images/adventure/skiinggulmarg.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Skiing is the best adventure thing you can do in Gulmarg, J&K. For those who love adrenaline junkies, skiing is what you can experience. The ski hill in Gulmarg is serviced by Gondola where the lift tips is about 4,000m with the summit of Mt. Apharwat. The ski terrain is unlimited offering alpine, chutes, cornices and glacier skiing. If you are an expert ski rider, then it will be the most thrilling experience.</p>
