		<h2>10. River Rafting in Tons Valley Uttarakhand:-</h2>
		<img src="images/adventure/riverraftingtons.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Located in Garhwal district of Uttarakhand, river Tons is the biggest tributary to Yamuna river. One can enjoy the Tons River expedition with river rafting which runs at the grade of 3-4 and sometime at 5 grades. During the rafting in Tone River, you can enjoy some famous rapids including 'One the Hornes of the Tons', 'Tones Squeeze', 'Confluence', 'Tuni Bazaar' and 'Major Surprise'.</p>
		<p><b>Location:</b> Tons Valley is located in Jaunsar Bawar region of Garhwal district.</p>
		<p><b>Price:</b> Tons Valley river rafting can be enjoyed while having a camping under the expedition. The camping can be the duration of 8N/9D with the price range of Rs 30,000/- per person.</p>
