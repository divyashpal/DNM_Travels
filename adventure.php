		<h2>19. Sandakphu Trek, West Bengal:-</h2>
		<img src="images/adventure/sandakphutrek.png" class="img-fluid" alt="adv" style="width:100%;" style="height:auto;">
		<p>Located at the altitude of 11,941 ft, Sandakphu Trek is the most happening adventure trip to have in Darjeeling, West Bengal. Known as the Trekkers Wonderland, you can participate in 5-6 days long trek expedition. The entire trek would take you to the picturesque location and giving you the view of the Everest, Kanchenjunga, Lhotse and Makalu.</p>
		<p><b>How to Reach:</b> You can reach Sandakphu by car in 4 hours from Darjeeling.</p>
