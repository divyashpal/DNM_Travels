	<div id="section13">
		<h2>13.Paragliding in Bir-Billing:-</h2>
		<img src="images/adventure/birbilling.png" alt="adv" class="img-fluid" style="width:100%;" style="height:auto;">
		<p>Bir-Billing Village is located in Palampur district of Himachal Pradesh and offers a great adventurous experience of paragliding. However, one has to patience enough and should not carry height phobia. Bir-Billing successfully hosted paragliding world cup in 2015. In fact, it is considered as the safest destination for paragliding where the take-off site is 8000ft from sea level and the landing site is 4300 ft.</p>
		<p><b>Price:</b> For per person the price of paragliding in Rs 2500/- and the duration will be 15-20 minutes.</p>
		<p><b>Best Time:</b> The best time for paragliding is from November to May.</p>
		<p><b>How to reach Bir Billing:</b> There are different routes to reach Bir Billing. Starting from Delhi you will cover Chandigarh, Kangra, Palampur and other regions, depending on the routes.</p>
