		<img src="images/adventure/girnationalpark.png" class="img-fluid" alt="adv" style="width:100%;" style="height:auto;">
		<p>Known as the Majestic home of Royal King, Gir National Park is the only place where one can spot lions roaming freely. The national park covers 1412 sq. km, and is the home of approx. 523 lions. Not only lions, one can come cross various birds, reptiles and other animals. Take a jeep safari throughout the park and get encountered with wild animals roaming freely. There are fun tour, Gir holiday tour and jeep safari tour which one can take benefit of.</p>
		<p><b>Location:</b> The park is situated in Junagadh district of Gujarat and is reachable from cities like Ahmedabad and Rajkot.</p>
		<p><b>Best Time:</b> Park remains open from September to May.</p>
		<p><b>Price:</b> For Jeep safari, the charges are Rs 4900 for 6 people in one jeep (Indian) and Rs 12400 for foreigners. The price includes jeep & Driver, Permit Charges, Guide Charges, One Camera Charges, Online Payment Gateway Charges, Pickup & Drop facility from resort.</p>
