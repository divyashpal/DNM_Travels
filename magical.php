		<h2>7.Varanasi - Uttar Pradesh</h2>
		<img src="images/magical/varanasighats.png" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Varanasi is a sacred, colorful and fascinating city located in the banks of the river Ganges in the Southeastern Uttar Pradesh. Varanasi is also called as 'Banaras' and 'City of lights'. It is also one of the oldest, continuously inhabited city in the world. Apart from the religious significance, Varanasi is renowned worldwide for its fantastic river frontage. Along the banks of the river Ganges, this city has numerous attractive temples, shrines and palaces.</p><p>Varanasi has 87 ghats or riverfront steps in total. Some of these ghats used for the cremation process. In such an atmosphere you will feel more spiritual. The ancient architecture and colors of the buildings in the Varanasi will also leave you awestruck.</p><p>One such building is  Kashi Vishwanath temple. It the most important temple in Varanasi dedicated to Lord Shiva. It is said to be more than 1 ton gold was used for the gold plating of this temple. When you enter the city you can crowded narrow streets. Taking a boat ride along the river Ganges is the best way to experience the Varanasi city. You can also enjoy stunning sunrise and sunset in this holy city.</p>
