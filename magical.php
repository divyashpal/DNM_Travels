	<div id="section8">
		<h2>8.Zanskar Valley - Ladakh</h2>
		<img src="images/magical/zanskarvalley.png" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>This breathtakingly beautiful valley is located in the Kargil district of Ladakh. Surrounded by snow-capped Himalayan peaks, Zanskar valley is situated at an altitude of 13154 above sea level. It is also the most remote of all trans-Himalayan valleys.</p><p>Another exciting sight you can enjoy on the trip to Zanskar valley is the confluence of the Zanskar River and Indus River. On the meeting point the dirty green waters of Indus River get mixed with the clean blue waters of the Zanskar River. Summer is the best time to visit the Zanskar valley. During winter, the Zanskar river freezes completely and the routes to this valley also get covered with snow during this time.</p>
	</div>

	<div id="section9">
		<h2>9.Nubra Valley - Ladakh</h2>
		<img src="images/magical/nubravalley.png" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>Located between Tibet and Kashmir and surrounded by snow capped peaks of the Himalayas, the Nubra valley is nothing shorter than a paradise. This picturesque valley is located at an altitude of 10000 feet above sea level. The famous Siachen glacier is located to the North of Nubra valley. It is the second longest glacier outside the polar regions.</p><p>To reach the Nubra valley, you have to cross the Khardung La Pass. At an altitude of 5602 meters, it is the highest motorable road in the world.The famous Shyok river and Siachen river also meet at this amazing valley. </p><p>The Nubra valley is also known as Moon land,  because in winter season this amazing valley looks like a landscape of the Moon. There is also a cold desert between Diskit and Hundur regions of Nubra valley. You can also see sand dunes and double humped Bactrian camels in this desert.</p>
	</div>

	<div id="section10">
		<h2>10.Pangong Tso Lake - Ladakh</h2>
		<img src="images/magical/pangongso.png" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>This insanely beautiful lake is located 160 km Southeast of Leh city. At an altitude of 4530, Pangong is one of the high altitude lakes in the Himalayan regions of India. This amazing 134 km long lake is extends from India to Tibet. The 60 percentage of the Pangong lake is lies within Tibetan range and remaining 40% lies within the Indian range.</p><p>This surreal looking lake is globally renowned for its bright blue waters and the scenic surroundings. Surprisingly, the water of the Pangong lake also displays different shades of blue, green and red at different period of time.</p><p>There is no fish or other aquatic life in Pangong lake due to the saltiness of its water. However the lakes receive numerous migratory birds in summer season. Despite of its saline water, the Pangong lake freezes completely in winter. May to September is the best time to visit the Pangong Lake.</p><p>Apart from its great natural beauty, Pangong lake is also a calm, pollution free place. The route to Pangong Tso is as beautiful as the lake. You have to pass the Chang La mountain pass of Ladakh. It is third highest motorable road in the world at at an altitude of 17352 feet above sea level. On the way you can see scenic landscape and majestic mountains.</p>

