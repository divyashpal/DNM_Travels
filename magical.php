		<h2>4.Golden Temple - Amritsar(Punjab):-</h2>
		<img src="images/magical/goldentemple.png" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>The Golden temple in Amritsar stands as a symbol of the oneness of God. It is the holiest shrine of Sikhs. Irrespective of religion, caste or place of living, people from all parts of the world are welcome here. Golden temple is also known as 'Harimandir Sahib', which means the temple of God.</p><p>Marble is the main material used for the construction of this temple and it is gold plated with pure gold. This amazing temple surrounded by a man-made lake named Amrit Sarovar. It is said to be the water of this holy lake have special properties.</p><p>The visitors can be entered the Golden temple from its four different sides. It symbolizes welcoming of people from all parts of the world, irrespective of their religion and cast. Surprisingly, the Golden temple was built on a lower level than its surroundings. So that visitors have to step down to reach the complex. It is to remind the visitors that humility is an important quality to lead a meaningful life.</p><p>Every day, around 35000 people fed for free at Golden temple. But all participants have to sit on the floor regardless of their religion or caste. This system at Golden temple symbolizes the equality.</p>
		
