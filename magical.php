		<h2>5.Valley Of Flowers - Chamoli(Uttarakhand):-</h2>
		<img src="images/magical/flowervalley.png" alt="adv" class="img-fluid" style="width :100%;" style="height:auto;">
		<p>How it possibly the view of a valley full of beautiful flowers at high peaks of Himalayas? Maybe it seems like a dream. But there is such a majestic valley in the high peaks of Zanskar ranges of Uttarakhand State. It's called the valley of flowers, A breathtaking Indian National Park and a world heritage site.</p><p>In a vast area of 87 square kilometers, this mystical valley is full of beautiful meadows of alpine flowers. The valley of home to more than 500 different types of wildflowers. It is also one of the few places in India that remain unspoiled by the human invasion. Nestled between the Great Himalayas and majestic Zanskar mountains, the valley of flowers is a heaven for nature lovers.</p><p>June to October is the best time to visit the valley of flowers. During this period, you can enjoy the breathtaking view of this valley at its full bloom.You have to climb a 3600-meter high mountain range to reach the valley of flowers, but totally worth it. The Nanda Devi Biosphere reserve, which includes the valley of flowers is a rich wilderness area in the Himalayas. It is home to a number of rare animal species like snow leopard, Asiatic black bear, and brown bear. You can also see beautiful stream and waterfalls on the way to the valley of flowers. </p>
	
