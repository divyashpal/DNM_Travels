		<h2>6.Alleppey Backwaters - Kerala</h2>
		<img src="images/magical/backwaters.png" class="img-fluid" style="width: 100%;" style="height: auto;">
		<p>The long stretch of backwaters of Kerala plays an important role in attracting global visitors to the state. With vast networks of rivers, lakes and lagoons, the great Kerala backwaters stretch over 1500 kilometers. Alleppey is an important link in this network. This romantic place has a long network of canals and lakes.</p><p>Among the international tourists, Alleppey is fondly called as ‘Venice of the East’. It is because, just like the Venice city, Alleppey has a vast network of canals. A glide in a houseboat through Alleppey backwaters also brings you almost same experience like the gondola ride through Venice canals.</p><p>There are many scenic routes to cruise on Alleppey backwaters. As it is a main region of Kerala backwaters, Nowadays Alleppey provide more than 120 houseboats for the tourists. In the banks of the canals of Alleppey, you can see vast expanse of paddy fields.</p>
